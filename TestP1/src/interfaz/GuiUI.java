    @Override
    public void showGame(GameState gameState){
        
        String sturno= Integer. toString(gameState.getCurrentPlayer());
        turno.setText(sturno);
        
        players.setText(gameState.getPlayers());
        monsters.setText(gameState.getMonsters());
        laberinto.setText(gameState.getLabyrinthv());
        logs.setText(gameState.getLog());  
        
        if(!(gameState.isWinner())){
            
            ganador.setText("Todavia no hay ning√∫n ganador");
        }
        else{
            
            ganador.setText("EL JUGADOR " + gameState.getCurrentPlayer() + " HA GANADO EL JUEGO");
        
        }
    }

